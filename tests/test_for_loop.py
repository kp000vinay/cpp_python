# Test for loops

# Simple for loop over list
for item in [1, 2, 3, 4, 5]:
    print(item)

# For loop with variable
my_list = [10, 20, 30]
for value in my_list:
    print(value)

# For loop with else clause
for x in [1, 2, 3]:
    print(x)
else:
    print("Loop completed")

# Nested for loops
for i in [1, 2, 3]:
    for j in [4, 5, 6]:
        print(i, j)

# For loop over range (simulated)
for num in [0, 1, 2, 3, 4]:
    print(num)

# For loop with dict
my_dict = {"a": 1, "b": 2, "c": 3}
for key in my_dict:
    print(key)


